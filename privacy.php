            <h2>DURATA DEI COOKIE</h2>
            <p>
                Alcuni cookie (cookie di sessione) restano attivi solo fino alla chiusura del browser. Altri cookie “sopravvivono” alla chiusura del browser e sono disponibili anche in successive visite dell’utente.
            </p>
            <p>
                Questi cookie sono detti persistenti e la loro durata è fissata dal server al momento della loro creazione. In alcuni casi è fissata una scadenza, in altri casi la durata è illimitata.
            </p>
            <p>
                <strong>Questo sito non fa uso di cookie persistenti.</strong> Tuttavia, navigando sulle pagine di questo sito, si può interagire con siti gestiti da terze parti che possono creare o modificare cookie permanenti e di profilazione.
